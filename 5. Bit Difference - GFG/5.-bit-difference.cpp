class Solution{
    public:
    // Function to find number of bits needed to be flipped to convert A to B
    int countBitsFlip(int a, int b){
        
        // Your logic here
        int xor_val=a^b;
        int set_bits=0;
        while(xor_val!=0)
        {
            xor_val=xor_val&(xor_val-1);
            set_bits++;
        }
        return set_bits;
        
    }
};

